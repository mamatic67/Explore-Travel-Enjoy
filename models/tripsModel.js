import mongoose from 'mongoose'

const tripsSchema = new mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true
    },
    description: {
        type: String,
        required: true
    },
    category: {
        type: String,
        required: true
    },
    content: {
        type: String,
        required: true
    },
    images: {
        type: Array,
        required: true
    },
}, {
    timestamps: true
})

let Dataset = mongoose.models.trips || mongoose.model('trips', tripsSchema)
export default Dataset